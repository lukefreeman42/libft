#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	void *p;

	p = (void*)s1;
	s1 += ft_strlen(s1);
	while (*s2)
		*s1++ = *s2++;
	*s1 = '\0';
	return ((char*)p);
}
