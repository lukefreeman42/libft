#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	void *p;

	p = (void*)s1;
	s1 += ft_strlen(s1);
	while (*s2)
	{
		if (n-- == 0)
		{
			*s1 = '\0';
			return ((char*)p);
		}
		*s1++ = *s2++;
	}
	*s1 = '\0';
	return ((char*)p);
}
